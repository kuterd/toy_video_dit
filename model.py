class ToyVideoDiT(nn.Module):
    def __init__(self, pw, ph, depth = 16, hidden_multiplier = 16):
        super().__init__()
        
        self.hidden_size = (ph * pw) * hidden_multiplier
        self.patch_size = ph
        
        self.in_channels = 1 * self.patch_size * self.patch_size
        self.out_channels = 1        
        self.img_in = nn.Linear(self.in_channels, self.hidden_size, bias=True)
        self.num_heads = 4
        pe_dim = self.hidden_size // self.num_heads
        print(pe_dim)
        self.embed_nd = EmbedND(pe_dim, 100_000, [pe_dim // 4, pe_dim // 4, pe_dim // 4, pe_dim // 4])
        self.blocks = torch.nn.ModuleList(
            [
                SingleStreamBlock(self.hidden_size, self.num_heads)
                for i in range(depth)
            ]
        )

        self.last_layer = LastLayer(self.hidden_size, self.patch_size, self.out_channels)
        self.time_in = MLPEmbedder(in_dim=256, hidden_dim=self.hidden_size)

    def forward(self, x: Tensor, x_ids: Tensor, timesteps: Tensor) -> Tensor:
        x = self.img_in(x)
        
        vec = self.time_in(timestep_embedding(timesteps, 256))

        pe = self.embed_nd(x_ids)

        for block in self.blocks:
            x = block(x, pe=pe, vec=vec)

        x = self.last_layer(x, vec=vec)
        return x
